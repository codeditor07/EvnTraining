public class Array_Linera_Traversal {
    public static void main(String[] args) {
        int []arr={10,20,30,40,50};
        int n=arr.length;

        System.out.println("Traversal:");
        for (int i =0 ; i<n; i++)
        {
            System.out.println(arr[i]);
        }
        System.out.println("Size of array is:"+n);
    }
}
